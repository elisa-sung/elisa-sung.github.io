// == (is equal to)
console.log(5 == 5)
// true
console.log(5 == 6)
// false
console.log("5" == 5)
// true
console.log("5"=== 5)
// false (checks data types, strict)


// > (is greater than)
console.log(5 > 4)
// true
console.log(5 > 5)
// false
console.log(5 > 6)
// false


// < (is less than)
console.log(5 < 4)
// false
console.log(5 < 5)
// false
console.log(5 < 6)
// true

//  >= (is greater than or equal to)
//  <= (is less than or equal to)
// != (not equal to)
console.log(5! = 5)
// false